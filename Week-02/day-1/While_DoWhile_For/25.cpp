#include <iostream>
#include <string>

using namespace std;

int main() {
	int a;
	// print the even numbers till 20

//============================================================================
	cout << "Done with WHILE: ";
	a = 0;
	while ( a <= 20 ) {
		if (a%2 == 0)
			cout << " " << a;
		a++;
	}
	cout << endl;
//============================================================================
	cout << "Done with DO-WHILE: ";
	a = 0;
	do {
		if (a%2 == 0)
			cout << " " << a;
		++a;
	} while ( a <= 20 );
	cout << endl;
//============================================================================
	cout << "Done with FOR: ";
	for (a = 0; a <= 20; a++ ){
		if (a%2 == 0)
			cout << " " << a;
	}
	cout << endl;
//============================================================================


	return 0;
}
