#include <iostream>
#include <string>

using namespace std;

int main() {
	int a;
	int ad = 6;
	// print the numbers till ad from 0

//============================================================================
	cout << "Done with WHILE: ";
	a = 0;
	while ( a <= ad ) {
		cout << " " << a;
		a++;
	}
	cout << endl;
//============================================================================
	cout << "Done with DO-WHILE: ";
	a = 0;
	do {
		cout << " " << a;
		++a;
	} while ( a <= ad );
	cout << endl;
//============================================================================
	cout << "Done with FOR: ";
	for (a = 0; a <= ad; a++ ){
		cout << " " << a;
	}
	cout << endl;
//============================================================================
	return 0;
}
